def get_strength_questions() -> List[Dict[str, Union[str, List[str]]]]:
    """
    Получение списка вопросов по сильным сторонам.
    
    Returns:
        List[Dict]: Список вопросов по сильным сторонам.
    """
    return STRENGTH_QUESTIONS

def get_strength_options_labels() -> Dict[str, str]:
    """
    Получение описаний для вариантов ответов на вопросы о сильных сторонах.
    
    Returns:
        Dict[str, str]: Словарь с описаниями вариантов ответов.
    """
