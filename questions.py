def get_all_questions() -> List[Dict[str, Union[str, List[str]]]]:
    """
    Получение всех вопросов (демо + сильные стороны).
    
    Returns:
        List[Dict]: Полный список вопросов.
    """
    return DEMO_QUESTIONS + STRENGTH_QUESTIONS

def get_question_by_id(question_id: str) -> Dict[str, Union[str, List[str]]]:
    """
    Получение вопроса по его ID.
    
    Args:
        question_id: ID вопроса.
        
    Returns:
        Dict: Данные вопроса или пустой словарь, если вопрос не найден.
    """
    for question in DEMO_QUESTIONS + STRENGTH_QUESTIONS:
        if question["id"] == question_id:
            return question
    return {} 