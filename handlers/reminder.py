    success = db.disable_reminder(db_user_id)
    
    if success:
        await message.answer(REMINDER_DISABLED_MESSAGE)
        logger.info(f"–ù–∞–ø–æ–º–∏–Ω–∞–Ω–∏—è –¥–ª—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è {user_id} –æ—Ç–∫–ª—é—á–µ–Ω—ã")
