        await message.answer(REMINDER_ALREADY_DISABLED_MESSAGE)
        logger.info(f"–ù–∞–ø–æ–º–∏–Ω–∞–Ω–∏—è –¥–ª—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è {user_id} —É–∂–µ –æ—Ç–∫–ª—é—á–µ–Ω—ã")
